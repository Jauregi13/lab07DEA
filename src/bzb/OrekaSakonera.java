package bzb;

public class OrekaSakonera {
		boolean oreka;
		int sakonera; 

		public OrekaSakonera() {
			this.oreka = true;
			this.sakonera = 0;
		}
		
		public boolean getOreka() {
			return this.oreka;
		}
		
		public int getSakonera() {
			return this.sakonera;
		}
		
		
		public void setOreka(boolean oreka) {
			this.oreka = oreka;
		}
		
		public void setSakonera(int sakonera) {
			this.sakonera = sakonera;
		}						

}
